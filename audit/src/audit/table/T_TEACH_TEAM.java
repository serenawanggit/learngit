package audit.table;

import audit.annotation.ExcelAnnotation;
import audit.annotation.ExcelTable;

@ExcelTable(table="T_TEACH_TEAM")
public class T_TEACH_TEAM {
	 @ExcelAnnotation(exportname="研究方向")
	 private String research_direction;
	 @ExcelAnnotation(exportname="负责人工号")
	 private String TEACHER_NUM;
	 @ExcelAnnotation(exportname="负责人姓名")
	 private String TEACHER_NAME;
	 @ExcelAnnotation(exportname="级别")
	 private String TLEVEL;
	 @ExcelAnnotation(exportname="评定年度")
	 private String ASSESS_YEAR;
	 @ExcelAnnotation(exportname="所属院系名称")
	 private String DEPARTMENT_NAME;
	public String getResearch_direction() {
		return research_direction;
	}
	public void setResearch_direction(String research_direction) {
		this.research_direction = research_direction;
	}
	public String getTEACHER_NUM() {
		return TEACHER_NUM;
	}
	public void setTEACHER_NUM(String tEACHER_NUM) {
		TEACHER_NUM = tEACHER_NUM;
	}
	public String getTEACHER_NAME() {
		return TEACHER_NAME;
	}
	public void setTEACHER_NAME(String tEACHER_NAME) {
		TEACHER_NAME = tEACHER_NAME;
	}
	public String getTLEVEL() {
		return TLEVEL;
	}
	public void setTLEVEL(String tLEVEL) {
		TLEVEL = tLEVEL;
	}
	public String getASSESS_YEAR() {
		return ASSESS_YEAR;
	}
	public void setASSESS_YEAR(String aSSESS_YEAR) {
		ASSESS_YEAR = aSSESS_YEAR;
	}
	public String getDEPARTMENT_NAME() {
		return DEPARTMENT_NAME;
	}
	public void setDEPARTMENT_NAME(String dEPARTMENT_NAME) {
		DEPARTMENT_NAME = dEPARTMENT_NAME;
	}
	 

}
