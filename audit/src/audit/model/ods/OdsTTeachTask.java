package audit.model.ods;

import java.io.Serializable;

public class OdsTTeachTask implements Serializable{
    /**
	 * 
	 * OdsTTeachTask.java
	 */
	private static final long serialVersionUID = -4010819597293327710L;

	private Integer id;

    private String syear;

    private String academeId;

    private Integer additionalUserId;

    private Integer additionalTemplateId;

    private Integer orgCheckUserId;

    private Integer colCheckUserId;

    public OdsTTeachTask(){}
    public OdsTTeachTask(Integer id, String syear, String academeId,
			Integer additionalUserId, Integer additionalTemplateId,
			Integer orgCheckUserId, Integer colCheckUserId) {
		super();
		this.id = id;
		this.syear = syear;
		this.academeId = academeId;
		this.additionalUserId = additionalUserId;
		this.additionalTemplateId = additionalTemplateId;
		this.orgCheckUserId = orgCheckUserId;
		this.colCheckUserId = colCheckUserId;
	}

	public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }


    public String getSyear() {
		return syear;
	}

	public void setSyear(String syear) {
		this.syear = syear;
	}

    public String getAcademeId() {
		return academeId;
	}

	public void setAcademeId(String academeId) {
		this.academeId = academeId;
	}

	public Integer getAdditionalUserId() {
        return additionalUserId;
    }

    public void setAdditionalUserId(Integer additionalUserId) {
        this.additionalUserId = additionalUserId;
    }

    public Integer getAdditionalTemplateId() {
        return additionalTemplateId;
    }

    public void setAdditionalTemplateId(Integer additionalTemplateId) {
        this.additionalTemplateId = additionalTemplateId;
    }

    public Integer getOrgCheckUserId() {
        return orgCheckUserId;
    }

    public void setOrgCheckUserId(Integer orgCheckUserId) {
        this.orgCheckUserId = orgCheckUserId;
    }

    public Integer getColCheckUserId() {
        return colCheckUserId;
    }

    public void setColCheckUserId(Integer colCheckUserId) {
        this.colCheckUserId = colCheckUserId;
    }
}