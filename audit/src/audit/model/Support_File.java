package audit.model;

import java.sql.Timestamp;

public class Support_File {

	private int id;
	
	private String filename;//文件名
	
	private String dir;//文件路径
	
	private int uploader;//上传人ID
	
	private String uploadername;//上传人名称
	
	private Timestamp uploadtime;//上传时间
	
	private int flag;//文件类型 1.文件  .doc .xls .ppt 2.图片 .jpg .png  3.视频
	
	private int status;//状态--默认为1
	
	private int pid;//方案ID

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public String getDir() {
		return dir;
	}

	public void setDir(String dir) {
		this.dir = dir;
	}

	public int getUploader() {
		return uploader;
	}

	public void setUploader(int uploader) {
		this.uploader = uploader;
	}

	public String getUploadername() {
		return uploadername;
	}

	public void setUploadername(String uploadername) {
		this.uploadername = uploadername;
	}

	public Timestamp getUploadtime() {
		return uploadtime;
	}

	public void setUploadtime(Timestamp uploadtime) {
		this.uploadtime = uploadtime;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	
	
	
	
}
