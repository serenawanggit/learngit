package audit.model.assess;

public class AsAccessScoreScaleDetail {
    private Integer id;

    private Integer scorScaleId;

    private Integer conformity;

    private Float selfScore;

    private Integer generalScoreId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getScorScaleId() {
        return scorScaleId;
    }

    public void setScorScaleId(Integer scorScaleId) {
        this.scorScaleId = scorScaleId;
    }

    public Integer getConformity() {
        return conformity;
    }

    public void setConformity(Integer conformity) {
        this.conformity = conformity;
    }



    public Float getSelfScore() {
		return selfScore;
	}

	public void setSelfScore(Float selfScore) {
		this.selfScore = selfScore;
	}

	public Integer getGeneralScoreId() {
        return generalScoreId;
    }

    public void setGeneralScoreId(Integer generalScoreId) {
        this.generalScoreId = generalScoreId;
    }
}