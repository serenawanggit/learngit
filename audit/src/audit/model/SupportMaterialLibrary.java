package audit.model;

import java.sql.Timestamp;

public class SupportMaterialLibrary {

	private int id;//主键
	
	private String filename;//文件名
	
	private String dir;//文件路径
	
	private int dutyPersonId;//责任人ID
	
	private String dutyPersonName;//责任人名称

	private String  organizationName;// 机构名称
	
	private Timestamp uploadtime;//上传时间
	
	private int flag;//文件类型 1.文件  .doc .xls .ppt 2.图片 .jpg .png  3.视频
	
	private int status;//状态--默认为1
	
	private int pid;//方案ID
	
	private Timestamp createTime;//创建时间

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public String getDir() {
		return dir;
	}

	public void setDir(String dir) {
		this.dir = dir;
	}

	public Timestamp getUploadtime() {
		return uploadtime;
	}

	public int getDutyPersonId() {
		return dutyPersonId;
	}

	public void setDutyPersonId(int dutyPersonId) {
		this.dutyPersonId = dutyPersonId;
	}

	public String getDutyPersonName() {
		return dutyPersonName;
	}

	public void setDutyPersonName(String dutyPersonName) {
		this.dutyPersonName = dutyPersonName;
	}

	public String getOrganizationName() {
		return organizationName;
	}

	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}

	public void setUploadtime(Timestamp uploadtime) {
		this.uploadtime = uploadtime;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public Timestamp getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Timestamp createTime) {
		this.createTime = createTime;
	}

}
