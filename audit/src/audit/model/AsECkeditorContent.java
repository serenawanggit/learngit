package audit.model;
/**
 * ckeditor保存内容实体
 * @author MOOC
 *
 */
public class AsECkeditorContent {
	
	private int id;
	
	//保存内容《html标签格式内容》
	private String content;
	
	//对应左边方案id
	private Integer eid;
	
	private int rtid;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getEid() {
		return eid;
	}

	public void setEid(Integer eid) {
		this.eid = eid;
	}

	public int getRtid() {
		return rtid;
	}

	public void setRtid(int rtid) {
		this.rtid = rtid;
	}
	
	
	
}
