package audit.model.gj;

import audit.annotation.ExcelAnnotation;

public class AS_GJ_FZJ {

	private Integer id;
	@ExcelAnnotation(exportname="合计")
	private String hj;
	@ExcelAnnotation(exportname="正高级")
	private String zgj;
	@ExcelAnnotation(exportname="副高级")
	private String fgj;
	@ExcelAnnotation(exportname="中级")
	private String zj;
	@ExcelAnnotation(exportname="初级")
	private String cj;
	@ExcelAnnotation(exportname="未定职级")
	private String wdzj;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getHj() {
		return hj;
	}

	public void setHj(String hj) {
		this.hj = hj;
	}

	public String getZgj() {
		return zgj;
	}

	public void setZgj(String zgj) {
		this.zgj = zgj;
	}

	public String getFgj() {
		return fgj;
	}

	public void setFgj(String fgj) {
		this.fgj = fgj;
	}

	public String getZj() {
		return zj;
	}

	public void setZj(String zj) {
		this.zj = zj;
	}

	public String getCj() {
		return cj;
	}

	public void setCj(String cj) {
		this.cj = cj;
	}

	public String getWdzj() {
		return wdzj;
	}

	public void setWdzj(String wdzj) {
		this.wdzj = wdzj;
	}
	
	public String toString(){
		StringBuffer sb = new StringBuffer();
		sb.append(getHj() + "|" + getZgj() + "|" + getFgj() 
				+ "|" + getZj() + "|" + getCj() + "|" + getWdzj()+ "|" + getId());
		return sb.toString();
	}
	
}
