package audit.model.gj;

import audit.annotation.ExcelAnnotation;

public class AS_GJ_QTXSQK {

	private Integer id;
	@ExcelAnnotation(exportname="结业生数合计")
	private String JYSSHJ;
	@ExcelAnnotation(exportname="结业女生数")
	private String JYNSS;
	@ExcelAnnotation(exportname="注册学生数合计")
	private String ZCXSHJ;
	@ExcelAnnotation(exportname="注册女生数")
	private String ZCNSS;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getJYSSHJ() {
		return JYSSHJ;
	}
	public void setJYSSHJ(String jYSSHJ) {
		JYSSHJ = jYSSHJ;
	}
	public String getJYNSS() {
		return JYNSS;
	}
	public void setJYNSS(String jYNSS) {
		JYNSS = jYNSS;
	}
	public String getZCXSHJ() {
		return ZCXSHJ;
	}
	public void setZCXSHJ(String zCXSHJ) {
		ZCXSHJ = zCXSHJ;
	}
	public String getZCNSS() {
		return ZCNSS;
	}
	public void setZCNSS(String zCNSS) {
		ZCNSS = zCNSS;
	}
	
	
	public String toString(){
		StringBuffer sb = new StringBuffer();
		sb.append(getJYSSHJ() + "|" + getJYNSS() + "|" + getZCXSHJ() + "|" + getZCNSS()+ "|" + getId());
		return sb.toString();
	}
	
}
