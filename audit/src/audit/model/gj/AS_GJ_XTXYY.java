package audit.model.gj;

import audit.annotation.ExcelAnnotation;

public class AS_GJ_XTXYY {

	private Integer id;
	@ExcelAnnotation(exportname = "合计")
	private String HJ;
	@ExcelAnnotation(exportname = "患病")
	private String HB;
	@ExcelAnnotation(exportname = "停学实践（求职）")
	private String TXSJ;
	@ExcelAnnotation(exportname = "贫困")
	private String PK;
	@ExcelAnnotation(exportname = "学习成绩不好")
	private String XXCJBH;
	@ExcelAnnotation(exportname = "出国")
	private String CG;
	@ExcelAnnotation(exportname = "其他")
	private String QT;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getHJ() {
		return HJ;
	}

	public void setHJ(String hJ) {
		HJ = hJ;
	}

	public String getHB() {
		return HB;
	}

	public void setHB(String hB) {
		HB = hB;
	}

	public String getTXSJ() {
		return TXSJ;
	}

	public void setTXSJ(String tXSJ) {
		TXSJ = tXSJ;
	}

	public String getPK() {
		return PK;
	}

	public void setPK(String pK) {
		PK = pK;
	}

	public String getXXCJBH() {
		return XXCJBH;
	}

	public void setXXCJBH(String xXCJBH) {
		XXCJBH = xXCJBH;
	}

	public String getCG() {
		return CG;
	}

	public void setCG(String cG) {
		CG = cG;
	}

	public String getQT() {
		return QT;
	}

	public void setQT(String qT) {
		QT = qT;
	}
	
	public String toString(){
		StringBuffer sb = new StringBuffer();
		sb.append(getHJ() + "|" + getHB() + "|" + getTXSJ() + "|" + 
				getPK() + "|" + getXXCJBH() + "|" + getCG() + "|" + getQT()+ "|" + getId());
		return sb.toString();
	}
}
