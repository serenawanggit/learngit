//添加操作列
function addTdHtml(idName){
	var tab = document.getElementById(idName);//获得表格
	var colsNum = tab.rows.item(0).cells.length;//表格的列数
	var num = document.getElementById(idName).rows.length;//表格当前的行数
	var rownum = num;
	tab.insertRow(rownum);
	for(var i=0;i<rownum;i++){
		tab.rows[i].insertCell(colsNum);//插入列
		if(i==0){
			tab.rows[i].cells[colsNum].innerHTML = "操作";
		}else{
			tab.rows[i].cells[colsNum].innerHTML = "<a href='javascript:deleteRow()'>删除</a>";
		}
	}
}

